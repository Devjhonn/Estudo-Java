package Introducao.EstruturasRepeticao;

public class DoWhile{
    // do while e quase a mesma coisa que o while, soque aqui independende de ser v ou f ele executa uma vez
    public static void main(String[] args) {
        int i = 10;
        do {
            System.out.println(i);
            i++;
        }
        while (i < 5);
    }
}
